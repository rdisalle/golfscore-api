CREATE TABLE golfscore_scores (
    id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    name text NOT NULL,
    course text NOT NULL,
    course_id INTEGER
        REFERENCES golfscore_courses(id) ON DELETE CASCADE NOT NULL,
    score_hole_one text NOT NULL,
    score_hole_two text NOT NULL,
    score_hole_three text NOT NULL,
    score_hole_four text NOT NULL,
    score_hole_five text NOT NULL,
    score_hole_six text NOT NULL,
    score_hole_seven text NOT NULL,
    score_hole_eight text NOT NULL,
    score_hole_nine text NOT NULL,
    score_hole_ten text NOT NULL,
    score_hole_eleven text NOT NULL,
    score_hole_twelve text NOT NULL,
    score_hole_thirteen text NOT NULL,
    score_hole_fourteen text NOT NULL,
    score_hole_fifteen text NOT NULL,
    score_hole_sixteen text NOT NULL,
    score_hole_seventeen text NOT NULL,
    score_hole_eighteen text NOT NULL,
    total_score text NOT NULL,
    to_par text NOT NULL,
    date_modified TIMESTAMPTZ NOT NULL DEFAULT now()
);